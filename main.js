// Resume Download Functionality
const modal = document.getElementById('resume-modal');
const resumeDownloadBtns = document.querySelectorAll('#resume-download, #hero-resume-download, #contact-resume-download');
const closeBtn = document.querySelector('.close');
const downloadPdfBtn = document.getElementById('download-pdf');
const downloadDocBtn = document.getElementById('download-doc');

// Open modal when resume download buttons are clicked
resumeDownloadBtns.forEach(btn => {
    btn.addEventListener('click', (e) => {
        e.preventDefault();
        modal.style.display = 'block';
        document.body.style.overflow = 'hidden';
    });
});

// Close modal
closeBtn.addEventListener('click', () => {
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
});

// Close modal when clicking outside
window.addEventListener('click', (e) => {
    if (e.target === modal) {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
});

// Download PDF resume
downloadPdfBtn.addEventListener('click', (e) => {
    e.preventDefault();
    downloadResume('pdf');
});

// Download DOC resume (will also download PDF since we only have PDF)
downloadDocBtn.addEventListener('click', (e) => {
    e.preventDefault();
    downloadResume('doc');
});

function downloadResume(format) {
    // Create a temporary link element
    const link = document.createElement('a');
    link.href = 'Aayush_Chandak_CV_2025.pdf';
    link.download = 'Aayush_Chandak_CV_2025.pdf';
    link.target = '_blank';
    
    // Append to body, click, and remove
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);

    // Close modal
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';

    // Show success message
    setTimeout(() => {
        if (format === 'doc') {
            alert('PDF resume downloaded successfully! Note: Only PDF format is available. You can convert it to Word format using online converters if needed.');
        } else {
            alert('PDF resume downloaded successfully!');
        }
    }, 500);
}

// Add keyboard navigation for modal
document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && modal.style.display === 'block') {
        modal.style.display = 'none';
        document.body.style.overflow = 'auto';
    }
});